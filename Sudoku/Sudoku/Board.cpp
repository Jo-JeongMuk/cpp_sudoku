void Board::printboard()
{
	char tmp[9][9] = { '0', };
	for (int i = 0; i < 9; i++)
		for (int j = 0; j < 9; j++)
		{
			if (worksheet[i][j] == 0)
				tmp[i][j] = ' ';
			else
				tmp[i][j] = worksheet[i][j] + '0';
		}
	int row = 2;

	for (int i = 0; i < 9; i++)
	{
		int col = 4;
		for (int j = 0; j < 9; j++)
		{
			board[row][j + col] = tmp[i][j];
			col += 3;
		}
		row += 2;
	}

	system("cls");
	for (int i = 0; i < 20; i++)
		cout << board[i] << endl;
}


